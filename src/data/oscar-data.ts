export const allYears = [...new Set(oscarMovies.map(m => m.year))].sort((a, b) => b - a);
export const allGenres = [...new Set(oscarMovies.flatMap(m => m.genre))].sort();
export const allCountries = [...new Set(oscarMovies.map(m => m.country))].sort();

export function getMoviesByYear(year: number): OscarMovie[] {
  return oscarMovies.filter(m => m.year === year);
}

export function getMoviesByCategory(category: string): OscarMovie[] {
  return oscarMovies.filter(m => m.awards.some(a => a.category === category));
}

export function getMostAwardedMovies(): OscarMovie[] {
  return [...oscarMovies].sort((a, b) => b.totalOscars - a.totalOscars);
}

export function searchMovies(query: string): OscarMovie[] {
  const q = query.toLowerCase();
  return oscarMovies.filter(m =>
    m.title.toLowerCase().includes(q) ||
    m.director.toLowerCase().includes(q) ||
    m.cast.some(c => c.toLowerCase().includes(q)) ||
    m.genre.some(g => g.toLowerCase().includes(q))
  );
}
